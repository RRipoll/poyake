	static public String checkImageList(String data) {

		data=setPatternName(data,patternImageName );
		data=setPatternLp(data,patternImageLp );
		data=setPatternState(data,patternImageState );
		data=setPatternImage(data,patternImage );
		
		return data;
	}		

	static public String setPatternImage(String data,Pattern pattern ) {
		if(null!=data && data.contains("<image")) {
			Matcher matcher = pattern.matcher(data);
			if (matcher.find()) {
				System.out.println("Full match: " + matcher.group(0));
				for (int i = 1; i <= matcher.groupCount(); i++) {
					String label=matcher.group(i);
					String number=label.replace("<image", "").replace(">", "");
					String image=getImage(Integer.parseInt(number));
					data=data.replace(label, image);
				}
			}
		}
		return data;
	}
			
